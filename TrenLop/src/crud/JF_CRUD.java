    private void loadForm(){
         tbSP.removeAll();
         try{
           TestConnection pt=new TestConnection();
           ResultSet rs=pt.GetData("data");
           int i=0;
           while(rs.next()){
               tbSP.setValueAt(rs.getString("ID"), i, 0);
               tbSP.setValueAt(rs.getString("Name"), i, 1);
               tbSP.setValueAt(rs.getString("Number"), i, 2);
               i++;
           }
           pt.Close();
       }catch(Exception ex){
          throw new RuntimeException(ex);
       }
    }
