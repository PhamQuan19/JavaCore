    private void tbSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSPMouseClicked
        int row= tbSP.getSelectedRow();
        String ma=tbSP.getValueAt(row, 0).toString();
        String ten=tbSP.getValueAt(row, 1).toString();
        String soLuong=tbSP.getValueAt(row, 2).toString();
        
        txtid.setText(ma);
        txtname.setText(ten);
        txtnumber.setText(soLuong);
    }//GEN-LAST:event_tbSPMouseClicked

